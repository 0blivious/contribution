package com.github.checkstyle.parser;

/**
 * Represents different states of severity of the checkstyle messages.
 *
 * @author atta_troll
 *
 */
public enum Severity {
    /**
     * Represents error level.
     */
    ERROR,

    /**
     * Represents warning level.
     */
    WARNING,

    /**
     * Represents informational level.
     */
    INFORMATIONAL;
}
